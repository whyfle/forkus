function translate(key, values = {}) {
    const lang = window.appLanguage || document.documentElement.lang || "es";
    let text = appTranslations[lang]?.[key] || key;
    Object.entries(values).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, v);
    });
    return text;
}

function applyTranslations() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        if (!key) return;
        const attr = el.dataset.i18nAttr || el.dataset.i18nPlaceholder || el.dataset.i18nTitle || el.dataset.i18nValue;
        const value = translate(key);
        if (attr) {
            el.setAttribute(attr, value);
        } else {
            el.textContent = value;
        }
    });
}

async function loadLanguageConfig() {
    try {
        const response = await fetch("/api/configuracion");
        if (!response.ok) return;
        const config = await response.json();
        const lang = config.idioma ?? "es";
        const tema = config.tema ?? "light";
        window.appLanguage = lang;
        document.documentElement.lang = lang;
        applyTranslations();
        applyTheme(tema);
        const selector = document.getElementById("idioma");
        if (selector) {
            selector.value = lang;
        }
        const temaSelector = document.getElementById("tema");
        if (temaSelector) {
            temaSelector.value = tema;
        }
    } catch (error) {
        console.error("Error loading language config:", error);
    }
}

function applyTheme(tema) {
    if (tema === "dark") {
        document.body.classList.add("dark");
    } else {
        document.body.classList.remove("dark");
    }
}

window.addEventListener("DOMContentLoaded", () => {
    loadLanguageConfig();
});
